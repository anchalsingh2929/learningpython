a=int(input("type your no. --"))
b=int(input("type your no. --"))
c=input(" what operation do you want ?? --")
if c=="multiply" or c=="*":
  print(a*b)
elif c=="divide" or c=="/":
  print(a/b)
elif c=="addition" or c=="+":
  print(a+b)
elif c=="subtraction" or c=="-":
  print(a-b)
else: 
   print("error")
