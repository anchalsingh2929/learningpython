def sum(a,b):
    return a+b 


def subtract(a,b):
    return (a-b)


def multiply(e,f):
    return(e*f)

def divide(g,h):
   return(g/h)

def power(y,z):
   return(y**z)

def less_than(w,r):
    if w<r:
        return False
    else: 
        return True    

def minimum(o,p):
    if o<p:
        return o
    else:    
        return p


def maximum(m,n):
    if m>n:
        return m
    else:
        return n

